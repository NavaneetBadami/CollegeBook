package com.college.memories.book.dto;

public class MemoriesSnapsBeans 
{
	private int snapId;
	private String snapName;
	private String uploadedUserName;
	private String uploadedFriendName;
	private String uploadedDate;
	private String uploadedTime;
	
	
	public int getSnapId() {
		return snapId;
	}
	public void setSnapId(int snapId) {
		this.snapId = snapId;
	}
	public String getSnapName() {
		return snapName;
	}
	public void setSnapName(String snapName) {
		this.snapName = snapName;
	}
	public String getUploadedUserName() {
		return uploadedUserName;
	}
	public void setUploadedUserName(String uploadedUserName) {
		this.uploadedUserName = uploadedUserName;
	}
	public String getUploadedFriendName() {
		return uploadedFriendName;
	}
	public void setUploadedFriendName(String uploadedFriendName) {
		this.uploadedFriendName = uploadedFriendName;
	}
	public String getUploadedDate() {
		return uploadedDate;
	}
	public void setUploadedDate(String uploadedDate) {
		this.uploadedDate = uploadedDate;
	}
	public String getUploadedTime() {
		return uploadedTime;
	}
	public void setUploadedTime(String uplodedTime) {
		this.uploadedTime = uplodedTime;
	}
	
	
	

}
